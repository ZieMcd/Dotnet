using System;

namespace For_Loops
{
    class Program
    {
        static void Main(string[] args)
        {

            int[] arrNums = { 76, 35, 4, 27, 9 };

            for (int i = 0; i < arrNums.Length; i++)
            {
                Console.WriteLine(arrNums[i]);
            }
        }
    }
}
